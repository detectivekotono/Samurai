//朝の挨拶を出力する関数を定義する
const sayGoodMorning = () => {
  console.log(`おはようございます！`);
  console.log(`昨日はよく眠れましたか？`);
  console.log(`今日も一日頑張りましょう！`);
};

//夜のあいさつを出力する関数を定義する
const sayGoodEvening = () => {
  console.log(`こんばんわ！`);
  console.log(`今日も一日おつかれさまでした。`);
};
//朝の挨拶を出力する関数を呼び出す
sayGoodMorning();

//夜の挨拶を出力する関数を呼び出す
sayGoodEvening();
